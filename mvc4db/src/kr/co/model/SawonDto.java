package kr.co.model;

public class SawonDto {
	private String sawon_no, sawon_name, buser_num, sawon_jik, sawon_pay, sawon_ibsail, sawon_gen;

	public String getSawon_no() {
		return sawon_no;
	}

	public void setSawon_no(String sawon_no) {
		this.sawon_no = sawon_no;
	}

	public String getSawon_name() {
		return sawon_name;
	}

	public void setSawon_name(String sawon_name) {
		this.sawon_name = sawon_name;
	}

	public String getBuser_num() {
		return buser_num;
	}

	public void setBuser_num(String buser_num) {
		this.buser_num = buser_num;
	}

	public String getSawon_jik() {
		return sawon_jik;
	}

	public void setSawon_jik(String sawon_jik) {
		this.sawon_jik = sawon_jik;
	}

	public String getSawon_pay() {
		return sawon_pay;
	}

	public void setSawon_pay(String sawon_pay) {
		this.sawon_pay = sawon_pay;
	}

	public String getSawon_ibsail() {
		return sawon_ibsail;
	}

	public void setSawon_ibsail(String sawon_ibsail) {
		this.sawon_ibsail = sawon_ibsail;
	}

	public String getSawon_gen() {
		return sawon_gen;
	}

	public void setSawon_gen(String sawon_gen) {
		this.sawon_gen = sawon_gen;
	}
	
}
